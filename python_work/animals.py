animals = ["horse", "cheETah", "Dog", "ORCA"]
for animal in animals:
    if animal.lower() == "horse":
        print(f"\nThe {animal.lower()} is one of the most important animals for human and cultural evolution.\n")
    elif animal.lower() == "cheetah":
        print(f"A {animal.lower()} has a ground top speed of 100mph.\n")
    elif animal.lower() == "dog":
        print(f"A good {animal.lower()} is the best companion.\n")
    else:
        print(f"There is an {animal.lower()} that sinks boats.\n")
print("All of these animals are mammals.\n")

    